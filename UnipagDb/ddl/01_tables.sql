  COD_IGREJA NUMBER(10),
  GRUPO VARCHAR2(64) DEFAULT '' NOT NULL,
  ID_CAMPANHA NUMBER(10),
  TIPO_DOACAO VARCHAR2(32) DEFAULT '' NOT NULL,
  VALOR NUMBER(10,2) DEFAULT 0.00 NOT NULL,
  STATUS VARCHAR2(32) DEFAULT '' NOT NULL,
  PROCESSADO NUMBER(1) NOT NULL
);

