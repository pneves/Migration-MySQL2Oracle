CREATE TABLE RAWINGESTEVENT (
  ID VARCHAR2(36) NOT NULL,
  TOPIC VARCHAR2(64) NOT NULL,
  RECEIVED_AT TIMESTAMP NOT NULL,
  PROCESSED_AT TIMESTAMP,
  REQUEST_PATH VARCHAR2(256) NOT NULL,
  HEADERS CLOB NOT NULL,
  PAYLOAD CLOB NOT NULL,
  PAYLOAD_SHA256 VARCHAR2(64),
  STATUS VARCHAR2(10) NOT NULL,
  ATTEMPTS NUMBER(10) NOT NULL,
  LAST_ERROR CLOB,
  SUCCESS_COUNT NUMBER(10),
  FAILED_COUNT NUMBER(10),
  FAILED_ITEMS CLOB,
  RETRY_ATTEMPTS NUMBER(10) DEFAULT 0 NOT NULL,
  LAST_RETRY_AT TIMESTAMP,
  CONSTRAINT CK_RAWINGESTEVENT_HEADERS_JSON CHECK (HEADERS IS JSON),
  CONSTRAINT CK_RAWINGESTEVENT_PAYLOAD_JSON CHECK (PAYLOAD IS JSON),
  CONSTRAINT CK_RAWINGESTEVENT_STATUS_ENUM CHECK (STATUS IN ('received', 'queued', 'processing', 'processed', 'failed') OR STATUS IS NULL),
  CONSTRAINT CK_RAWINGESTEVENT_FAILED_ITEMS CHECK (FAILED_ITEMS IS JSON)
);

