CREATE TABLE OFERTAS (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  COD_IGREJA NUMBER(10) NOT NULL,
  ID_GRUPO NUMBER(10) NOT NULL,
  PROJETO_ID NUMBER(10) NOT NULL,
  GRUPO VARCHAR2(64),
  APOIO VARCHAR2(8),
  META NUMBER(10,2) DEFAULT 0.00 NOT NULL,
  STATUS VARCHAR2(10),
  DATAINICIO TIMESTAMP,
  DATAFIM TIMESTAMP,
  DESCRICAO VARCHAR2(500),
  NOME VARCHAR2(128),
  TITULO VARCHAR2(300),
  ULTIMA_ATUALIZACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  SALDO_FINAL NUMBER(10,2) DEFAULT 0.00,
  DATA_FINALIZACAO TIMESTAMP,
  CONSTRAINT CK_OFERTAS_APOIO_ENUM CHECK (APOIO IN ('cenaculo', 'bloco', 'regiao', 'estado') OR APOIO IS NULL),
  CONSTRAINT CK_OFERTAS_STATUS_ENUM CHECK (STATUS IN ('ativo', 'inativo', 'finalizado', 'cancelado', 'pausado') OR STATUS IS NULL)
