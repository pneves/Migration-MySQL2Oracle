-- 01_tables.sql - tabelas convertidas de MySQL para Oracle

CREATE TABLE ACTIVITYLOG (
  ACTIVITY_LOG_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  CREATED_AT TIMESTAMP NOT NULL,
  ACTOR_USER_ID NUMBER(10),
  ACTION VARCHAR2(50) NOT NULL,
  ENTITY_TYPE VARCHAR2(255),
  ENTITY_ID VARCHAR2(255),
  ROUTE VARCHAR2(512),
  METHOD VARCHAR2(16),
  STATUS_CODE NUMBER(10),
  IP VARCHAR2(64),
  USER_AGENT VARCHAR2(512),
  REQUEST_ID VARCHAR2(128),
  BEFORE CLOB,
  AFTER CLOB,
  META CLOB,
  RELATED_ACTIVITY_ID NUMBER(10),
  CONSTRAINT CK_ACTIVITYLOG_BEFORE_JSON CHECK (BEFORE IS JSON),
  CONSTRAINT CK_ACTIVITYLOG_AFTER_JSON CHECK (AFTER IS JSON),
  CONSTRAINT CK_ACTIVITYLOG_META_JSON CHECK (META IS JSON)
);

CREATE TABLE ENTRADA (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  DATETIME_DOACAO TIMESTAMP NOT NULL,
  CODIGO NUMBER(10),
  DOADOR VARCHAR2(128) DEFAULT '',
