-- 01_tables.sql - tabelas convertidas de MySQL para Oracle/Exadata

CREATE TABLE ENTRADAS (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  DATETIME_DOACAO TIMESTAMP NOT NULL,
  CODIGO NUMBER(10),
  DOADOR VARCHAR2(128) DEFAULT '',
  TELEFONE VARCHAR2(24) DEFAULT '',
  UF VARCHAR2(24) DEFAULT '' NOT NULL,
  BLOCO VARCHAR2(64) DEFAULT '',
  REGIAO VARCHAR2(64) DEFAULT '',
  CENACULO VARCHAR2(64) DEFAULT '' NOT NULL,
  GRUPO VARCHAR2(64) DEFAULT '' NOT NULL,
  ID_PROJETO NUMBER(10),
  TIPO_DOACAO VARCHAR2(32) DEFAULT '' NOT NULL,
  VALOR NUMBER(10,2) DEFAULT 0.00 NOT NULL,
  STATUS VARCHAR2(32) DEFAULT '' NOT NULL,
  OFERTA NUMBER(10,2) DEFAULT 0.00 NOT NULL
);

CREATE TABLE GRUPOS (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  ID_PROJETO NUMBER(10) NOT NULL,
  ID_CAMPANHA NUMBER(10),
  NOME VARCHAR2(64) NOT NULL,
  LAST_UPDATE TIMESTAMP NOT NULL,
  DESCRICAO VARCHAR2(128) DEFAULT '',
  UF VARCHAR2(4),
  BLOCO VARCHAR2(64),
  REGIAO VARCHAR2(64),
  CENACULO VARCHAR2(64),
  CENACULO_ID NUMBER(10),
  META NUMBER(10,2) DEFAULT 0.00,
  IS_FINISHED NUMBER(1),
  IS_OFERTA NUMBER(1)
);

CREATE TABLE IGREJAS (
  ID NUMBER(10) NOT NULL,
  COD_PAIS VARCHAR2(3),
  DEN_PAIS VARCHAR2(50) DEFAULT 'BRASIL' NOT NULL,
  COD_ESTADO NUMBER(10),
  DEN_ESTADO VARCHAR2(50) NOT NULL,
  DEN_REGIAO VARCHAR2(50) NOT NULL,
  DEN_AREA VARCHAR2(50) NOT NULL,
  COD_IGREJA NUMBER(10) NOT NULL,
  DEN_IGREJA VARCHAR2(50) NOT NULL,
  DEN_TIP_IGREJA VARCHAR2(20),
  DEN_TIP_OPERACAO VARCHAR2(20),
  DT_INI_IGREJA DATE,
  DT_ATUALIZACAO DATE,
  COD_AREA NUMBER(10),
  COD_REGIAO NUMBER(10),
  COD_TIP_IGREJA NUMBER(10),
  SGU_STATUS VARCHAR2(20),
  DEN_CNPJ VARCHAR2(22)
);

CREATE TABLE IMAGES (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  GROUP_NAME VARCHAR2(64) NOT NULL,
  PATH VARCHAR2(256) NOT NULL,
  TYPE VARCHAR2(32) NOT NULL,
  COD NUMBER(10)
);

CREATE TABLE LOCALIDADES (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  COD_IGREJA NUMBER(10),
  COD_GRUPO NUMBER(10),
  GRUPO VARCHAR2(64),
  GRUPO_ORIGINAL VARCHAR2(64),
  UF VARCHAR2(4) NOT NULL,
  BLOCO VARCHAR2(64),
  REGIAO VARCHAR2(64),
  CENACULO VARCHAR2(64),
  NOMENCLATURA VARCHAR2(32),
  RESERVA_OFERTA NUMBER(5,2) DEFAULT 50.00 NOT NULL,
  RESERVA_IGREJA NUMBER(5,2) DEFAULT 50.00 NOT NULL,
  DATA_FIM TIMESTAMP,
  DATA_INICIO TIMESTAMP,
  IS_OFERTA NUMBER(1)
);

CREATE TABLE OFERTAS (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  ID_IGREJA NUMBER(10) NOT NULL,
  ID_TIPO NUMBER(10),
  ID_GRUPO NUMBER(10),
  GRUPO VARCHAR2(64),
  APOIO VARCHAR2(8) NOT NULL,
  STATUS VARCHAR2(10),
  META NUMBER(10,2),
  DATAINICIO TIMESTAMP,
  DATAFIM TIMESTAMP,
  DESCRICAO VARCHAR2(500),
  NOME VARCHAR2(128),
  TITULO VARCHAR2(128) DEFAULT '',
  CONSTRAINT CK_OFERTAS_APOIO_ENUM CHECK (APOIO IN ('cenaculo', 'bloco', 'regiao', 'estado') OR APOIO IS NULL),
  CONSTRAINT CK_OFERTAS_STATUS_ENUM CHECK (STATUS IN ('ativo', 'inativo', 'finalizado', 'cancelado', 'pausado') OR STATUS IS NULL)
);

CREATE TABLE PROJETO (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  NOME VARCHAR2(64) NOT NULL,
  IS_FINISHED NUMBER(1),
  IS_OFERTA NUMBER(1),
  SUBGRUPO NUMBER(1),
  DESCRICAO VARCHAR2(128) DEFAULT ''
);

CREATE TABLE SAIDAS (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  EXTRATO NUMBER(10),
  TRANSACAO VARCHAR2(16),
  COMPROVANTE NUMBER(10),
  DATETIME_SAIDA TIMESTAMP NOT NULL,
  CONTA NUMBER(10),
  CONTA_APELIDO VARCHAR2(128) DEFAULT '',
  CONTA_REGIONAL NUMBER(10),
  CONTA_REGIONAL_APELIDO VARCHAR2(128) DEFAULT '',
  CONTA_IGREJA NUMBER(10) NOT NULL,
  CONTA_IGREJA_APELIDO VARCHAR2(128) DEFAULT '' NOT NULL,
  UF VARCHAR2(2) DEFAULT '' NOT NULL,
  DESCRICAO_TRANSACAO VARCHAR2(256) DEFAULT '' NOT NULL,
  VALOR_APROVADO NUMBER(10,2) DEFAULT 0.00 NOT NULL,
  CLASSIFICACAO_DESPESA_COD VARCHAR2(16) DEFAULT '',
  CLASSIFICACAO_DESPESA VARCHAR2(64) DEFAULT '',
  CENTRO_CUSTO_COMPRA_COD NUMBER(10),
  CENTRO_CUSTO_COMPRA VARCHAR2(64) DEFAULT '',
  COMPROVANTES_MULTIPLOS VARCHAR2(8) DEFAULT '',
  QTD_COMPROVANTES NUMBER(10) DEFAULT 0,
  DESCRICAO_COMPROVANTE VARCHAR2(256),
  STATUS_TRANSACAO VARCHAR2(24) DEFAULT '' NOT NULL,
  STATUS_AVALIACAO_COMPROVANTE VARCHAR2(24) DEFAULT '',
  DATA_ENVIO_COMPROVANTE VARCHAR2(24) DEFAULT '',
  ULTIMA_AAALISE_ANALISTA VARCHAR2(64) DEFAULT '',
  ULTIMA_ANALISE_MENSAGEM VARCHAR2(256) DEFAULT '',
  ULTIMA_ANALISE VARCHAR2(24) DEFAULT '',
  INSTANCIA NUMBER(10) DEFAULT 0,
  BLOCO VARCHAR2(64) DEFAULT '' NOT NULL
);

CREATE TABLE SALDOS (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  ID_GRUPO NUMBER(10) NOT NULL,
  NOME_GRUPO VARCHAR2(64) NOT NULL,
  ID_CENACULO NUMBER(10) NOT NULL,
  ID_ENTRADA NUMBER(10) NOT NULL,
  SALDO NUMBER(10,2) DEFAULT 0.00 NOT NULL,
  ENTRADA NUMBER(10,2) DEFAULT 0.00 NOT NULL,
  SAIDA NUMBER(10,2) DEFAULT 0.00 NOT NULL,
  VALUE NUMBER(10,2) DEFAULT 0.00 NOT NULL,
  DATA TIMESTAMP NOT NULL
);

CREATE TABLE SUBGRUPOS (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  ID_PRIMARIO NUMBER(10) NOT NULL,
  ID_SECUNDARIO NUMBER(10) NOT NULL
);

CREATE TABLE TREE (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  NAME VARCHAR2(256)
);

CREATE TABLE USERS (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  PASSWORD VARCHAR2(256),
  REPRESENTANTE VARCHAR2(256),
  EMAIL VARCHAR2(256),
  GRUPO VARCHAR2(64),
  NACIONAL NUMBER(1),
  UF VARCHAR2(2),
  STATUS NUMBER(10),
  BLOCO VARCHAR2(64) DEFAULT 'all',
  REGIAO VARCHAR2(64) DEFAULT 'all',
  RELATORIO NUMBER(1)
);

CREATE TABLE PC_EXPENSE_INGEST_STATE (
  ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  DAY_DATE DATE NOT NULL,
  SOURCE VARCHAR2(32) NOT NULL,
  STATUS VARCHAR2(20) NOT NULL,
  LAST_RUN TIMESTAMP NOT NULL,
  ERROR CLOB
);

